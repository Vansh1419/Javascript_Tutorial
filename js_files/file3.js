// Rule for naming variable

// you cannot start with numbers example:-
// 1value (invalid)
// value (valid)
var value1 = 10;
console.log(value1 ** 4);

// you can use only underscore(_) or dollor sign($) example:-
// first_name (valid)
// _firstname (valid)
// first$name (valid)
// $firstname (valid)

var first_name = 20; // Snake_case writting 
console.log(first_name);
var _firstname = 30;
console.log(_firstname);
var first$name = 40;
console.log(first$name);
var $firstname = 50;
console.log($firstname);

// you cannot use spaces
// first name (invalid)

// Convention 
// Start with small letter and use camelCase
var firstName = "vansh";
console.log(firstName);
